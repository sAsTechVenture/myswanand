function FloatingLanguageSwitcherContent() {
  const pathname = usePathname();
  const router = useRouter();
  const searchParams = useSearchParams();
  const currentLocale = getCurrentLocale(pathname);

  const changeLanguage = (locale: Locale) => {
    if (currentLocale === locale) return;
    let pathWithoutLocale = pathname.replace(/^\/(en|hi|mr)/, '');
    if (!pathWithoutLocale || pathWithoutLocale === '/') pathWithoutLocale = '';
    const queryString = searchParams.toString();
    const newPath = `/${locale}${pathWithoutLocale}${queryString ? `?${queryString}` : ''}`;
    router.replace(newPath);
  };

  return (
    <Popover>
      <PopoverTrigger asChild>
        <Button
          variant="ghost"
          size="icon"
          className="rounded-full w-14 h-14 flex items-center justify-center shadow-lg bg-white border border-gray-200 hover:bg-gray-50 transition-transform hover:scale-110 active:scale-95"
        >
          <Globe className="w-6 h-6" style={{ color: colors.green }} />
        </Button>
      </PopoverTrigger>
      <PopoverContent
        className="w-44 p-1.5"
        align="end"
        side="left"
        sideOffset={12}
      >
        <div className="flex flex-col gap-0.5">
          {locales.map((locale) => (
            <button
              key={locale}
              onClick={(e) => {
                e.preventDefault();
                e.stopPropagation();
                changeLanguage(locale);
              }}
              className={`px-3 py-2.5 text-sm rounded-md transition-all text-left font-medium ${
                currentLocale === locale
                  ? 'text-white shadow-sm'
                  : 'hover:bg-accent text-gray-700'
              }`}
              style={
                currentLocale === locale
                  ? { backgroundColor: colors.primary, color: colors.white }
                  : {}
              }
            >
              {localeNames[locale]}
            </button>
          ))}
        </div>
      </PopoverContent>
    </Popover>
  );
}

export function FloatingActionButtons() {
  const whatsappNumber = getContactPhoneNumberWhatsApp();
  const whatsappUrl = `https://wa.me/${whatsappNumber}`;

  return (
    <div className="fixed bottom-6 right-6 z-50 flex flex-col gap-3">
      {/* WhatsApp Button */}
      <Link
        href={whatsappUrl}
        target="_blank"
        rel="noopener noreferrer"
        aria-label="Chat on WhatsApp"
        className="w-14 h-14 rounded-full flex items-center justify-center shadow-lg transition-transform hover:scale-110 active:scale-95"
        style={{ backgroundColor: '#25D366' }}
      >
        <WhatsAppIcon className="w-7 h-7 text-white" />
      </Link>

      {/* Language Switcher */}
      <Suspense
        fallback={
          <div className="w-14 h-14 rounded-full flex items-center justify-center shadow-lg bg-white border border-gray-200">
            <Globe className="w-6 h-6 animate-pulse" style={{ color: colors.green }} />
          </div>
        }
      >
        <FloatingLanguageSwitcherContent />
      </Suspense>
    </div>
  );
}
